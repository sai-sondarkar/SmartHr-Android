package edu.itm.aiforhrrecruit.model;

public class ProfileModel {

    public String profile;
    public String srno;
    public String profile_competencies;
    public String teaml_competencies;


    public ProfileModel() {
    }

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getSrno() {
        return srno;
    }

    public void setSrno(String srno) {
        this.srno = srno;
    }

    public String getProfile_competencies() {
        return profile_competencies;
    }

    public void setProfile_competencies(String profile_competencies) {
        this.profile_competencies = profile_competencies;
    }

    public String getTeaml_competencies() {
        return teaml_competencies;
    }

    public void setTeaml_competencies(String teaml_competencies) {
        this.teaml_competencies = teaml_competencies;
    }
}
